var AdminTopics = {
  init : function() {
        
    $(".action_links a.block").click(function() {
      return confirm(TRANSLATIONS['BLOCK_TOPIC']);
    });

    $(".action_links a.unblock").click(function() {
      return confirm(TRANSLATIONS['UNBLOCK_TOPIC']);
    });
    
    $(".element_list .block").click(function() {
      return confirm(TRANSLATIONS['BLOCK_ELEMENT']);
    });

    $(".element_list a.unblock").click(function() {
      return confirm(TRANSLATIONS['UNBLOCK_ELEMENT']);
    });
    
    $(".action_links a.remove_topic").click(function() {
      return confirm(TRANSLATIONS['REMOVE_TOPIC']);
    });

    $(".element_list a.remove_element").click(function() {
      return confirm(TRANSLATIONS['REMOVE_ELEMENT']);
    });
  }
  
}

$(AdminTopics.init);